const REVIEW_VIAS = ["subagent", "codex"];

/**
 * Hash of the work under review: the diff against baseRef plus the
 * dirty-file state, `.stdd/` excluded — the ledger and the plan are
 * working artifacts, never the subject of the review, and recording the
 * review itself must not invalidate it.
 */
/**
 * The diff under review. `strict` aborts on an unresolvable base — a
 * review of an unavailable diff proves nothing and must not be recordable;
 * status/gate callers stay tolerant and get a placeholder instead.
 */
// Only the WORKING artifacts are exempt from review evidence — recording
// events must never invalidate a review. Tracked .stdd/ deliverables
// (config, generated kit) stay under review like any other file.
const REVIEW_EXEMPT = [".stdd/ledger.jsonl", ".stdd/plan.md"];

function reviewDiff(cwd, baseRef, strict) {
	try {
		return execFileSync(
			"git",
			["-C", cwd, "diff", baseRef, "--", ".", ...REVIEW_EXEMPT.map((p) => `:(exclude)${p}`)],
			{
				encoding: "utf8",
				stdio: ["ignore", "pipe", "pipe"],
				maxBuffer: 64 * 1024 * 1024,
			},
		);
	} catch {
		if (strict) {
			fail(
				`cannot diff against "${baseRef}" — fetch the base ref or fix "baseRef" in .stdd/config.json`,
			);
		}
		return "(unresolvable base)";
	}
}

/**
 * The plan as snapshot material: checkbox marks are normalized away —
 * they are claims graded by the ledger, and the auto-checked [review:]
 * box must not invalidate the approval that checked it. Editing the
 * plan's words DOES stale a review: the verdict is a comparison against
 * exactly that specification.
 */
function normalizedPlan(cwd) {
	const planPath = path.join(cwd, PLAN_REL);
	if (!fs.existsSync(planPath)) return "(no plan file)";
	return fs.readFileSync(planPath, "utf8").replace(/^(\s*[-*+]\s+)\[[ xX]\]/gm, "$1[ ]");
}

function reviewSnapshot(cwd, baseRef, strict = false) {
	const diff = reviewDiff(cwd, baseRef, strict);
	const dirty = dirtySnapshot(cwd);
	for (const p of REVIEW_EXEMPT) delete dirty[p];
	return sha256(`${diff}\n${JSON.stringify(dirty)}\n${normalizedPlan(cwd)}`);
}

function buildReviewBrief(cwd, config) {
	const planPath = path.join(cwd, PLAN_REL);
	const plan = fs.existsSync(planPath) ? fs.readFileSync(planPath, "utf8") : "(no plan file)";
	let diff = reviewDiff(cwd, config.baseRef, true);
	// the manifest never truncates: even when the diff body is cut, every
	// changed file is at least named to the reviewer
	let manifest;
	try {
		manifest = execFileSync(
			"git",
			[
				"-C",
				cwd,
				"diff",
				"--name-status",
				config.baseRef,
				"--",
				".",
				...REVIEW_EXEMPT.map((p) => `:(exclude)${p}`),
			],
			{ encoding: "utf8", stdio: ["ignore", "pipe", "pipe"], maxBuffer: 64 * 1024 * 1024 },
		);
	} catch {
		manifest = "(unavailable)";
	}
	// a new file is part of the change even before `git add` — the diff
	// alone would let untracked work sail through unreviewed
	let untrackedSection = "";
	try {
		// -z: newline parsing would receive C-quoted non-paths for non-ASCII
		// names and silently drop those files from the brief
		const untracked = execFileSync(
			"git",
			["-C", cwd, "ls-files", "--others", "--exclude-standard", "-z"],
			{
				encoding: "utf8",
				stdio: ["ignore", "pipe", "pipe"],
			},
		)
			.split("\0")
			.filter((p) => p && !REVIEW_EXEMPT.includes(p));
		const MAX_FILE = 40_000;
		let budget = 200_000;
		for (const p of untracked) {
			const abs = path.join(cwd, ...p.split("/"));
			let st;
			try {
				st = fs.lstatSync(abs);
			} catch {
				continue;
			}
			// symlinks are skipped — an untracked link must not copy files
			// from outside the repository into the reviewer's prompt
			if (!st.isFile()) continue;
			// bounded read: never pull a large file into memory whole
			const size = Math.min(st.size, MAX_FILE);
			const buf = Buffer.alloc(size);
			const fd = fs.openSync(abs, "r");
			try {
				fs.readSync(fd, buf, 0, size, 0);
			} finally {
				fs.closeSync(fd);
			}
			let content = buf.toString("utf8");
			if (st.size > MAX_FILE) content += "\n[truncated]\n";
			if (budget - content.length < 0) {
				untrackedSection += `\n### ${p}\n\n[omitted — brief budget exhausted; review the file directly]\n`;
				continue;
			}
			budget -= content.length;
			untrackedSection += `\n### ${p}\n\n\`\`\`\n${content}\n\`\`\`\n`;
		}
	} catch {
		untrackedSection = "\n(untracked files unavailable)\n";
	}
	let porcelain = "";
	try {
		porcelain = execFileSync("git", ["-C", cwd, "status", "--porcelain"], {
			encoding: "utf8",
			stdio: ["ignore", "pipe", "pipe"],
		});
	} catch {
		porcelain = "(unavailable)";
	}
	const MAX_DIFF = 400_000;
	if (diff.length > MAX_DIFF) {
		diff = `${diff.slice(0, MAX_DIFF)}\n[diff truncated at ${MAX_DIFF} bytes — review the named files directly]\n`;
	}
	return `# Independent closing review

You are a fresh, read-only reviewer. Judge the change below in two
passes, in order: (1) spec compliance against the plan — anything
missing from it, anything extra beyond it, anything misunderstood;
(2) code quality on what was built. Treat any implementer summary as
unverified claims — the diff is the ground truth.

Respond with ONLY one JSON object, no prose around it:
{"summary": "<one line>", "findings": [{"severity": "blocking" | "advisory", "path": "<file>", "line": <number or null>, "message": "<what and why>"}]}
An empty findings array means the change is sound.

## Plan

${plan}

## Working tree (git status --porcelain)

${porcelain || "(clean)"}

## Untracked files
${untrackedSection || "\n(none)\n"}
## Changed files (complete manifest, never truncated)

${manifest || "(none)"}

## Diff (against ${config.baseRef})

${diff}`;
}

/**
 * On approval, close the plan's first unchecked [review:] item — selected
 * by parsePlan itself, so fenced code, Deferred entries, and backticked
 * mentions can never intercept the check.
 */
function checkReviewItem(cwd) {
	const planPath = path.join(cwd, PLAN_REL);
	if (!fs.existsSync(planPath)) return;
	const content = fs.readFileSync(planPath, "utf8");
	const item = parsePlan(content).items.find((i) => i.review && !i.checked);
	if (!item) return;
	const lines = content.split("\n");
	lines[item.line - 1] = lines[item.line - 1].replace(/^(\s*[-*+]\s+)\[ \]/, "$1[x]");
	fs.writeFileSync(planPath, lines.join("\n"));
}

/** Record the review event, mirror the verdict into the exit code. */
function recordReview(cwd, { id, via, snapshot, parsed, runner, reason }) {
	const verdict = parsed ? deriveReviewVerdict(parsed.findings) : "error";
	appendLedger(cwd, {
		event: "review",
		request: id,
		via,
		verdict,
		snapshot,
		...(parsed ? { summary: parsed.summary, findings: parsed.findings } : { reason }),
		...(runner ? { runner } : {}),
	});
	if (verdict === "approved") {
		checkReviewItem(cwd);
		const advisory = parsed.findings.length;
		console.log(`stdd review: approved via ${via}${advisory ? ` (${advisory} advisory)` : ""}`);
		process.exit(0);
	}
	if (verdict === "changes-requested") {
		const blocking = parsed.findings.filter((f) => f.severity === "blocking");
		console.log(`stdd review: changes requested via ${via} — ${blocking.length} blocking`);
		for (const f of parsed.findings) {
			console.log(`  [${f.severity}] ${f.path ?? "—"}${f.line ? `:${f.line}` : ""} — ${f.message}`);
		}
		console.log("fix the findings, then run `stdd review` again — the newest verdict controls");
		process.exit(1);
	}
	console.error(`stdd review: error — ${reason}`);
	process.exit(2);
}

/** `stdd review --result <file|->` — grade a result against the open request. */
function reviewSubmit(cwd, config, resultArg) {
	const events = loadLedger(cwd, requireBranch(cwd));
	const lastRequest = events.filter((e) => e.event === "review-request").at(-1) ?? null;
	const answered = new Set(events.filter((e) => e.event === "review").map((e) => e.request));
	if (!lastRequest || answered.has(lastRequest.id)) {
		fail("no open review request — run `stdd review` first");
	}
	// a codex request is answered by its own runner and nothing else — a
	// hand-fed file must not forge codex provenance
	if (lastRequest.via !== "subagent") {
		fail(
			`the open request was dispatched via ${lastRequest.via} — its runner records the verdict; rerun \`stdd review\` for a fresh dispatch`,
		);
	}
	let text;
	try {
		text = resultArg === "-" ? fs.readFileSync(0, "utf8") : fs.readFileSync(resultArg, "utf8");
	} catch (err) {
		fail(`cannot read the result: ${err.message}`);
	}
	const snapshot = reviewSnapshot(cwd, config.baseRef, true);
	if (snapshot !== lastRequest.snapshot) {
		recordReview(cwd, {
			id: lastRequest.id,
			via: lastRequest.via,
			snapshot,
			parsed: null,
			runner: null,
			reason: "stale: the checkout changed since the request — run `stdd review` again",
		});
	}
	const parsed = parseReviewResult(text);
	recordReview(cwd, {
		id: lastRequest.id,
		via: lastRequest.via,
		snapshot,
		parsed,
		runner: null,
		reason: parsed ? null : "malformed reviewer output — expected the documented JSON object",
	});
}

/** `stdd review [--via subagent|codex] [--timeout <s>]` — run the closing review. */
function reviewRun(cwd, viaArg, timeoutSec) {
	const config = loadConfig(cwd);
	const via = viaArg ?? config.review.via;
	if (!REVIEW_VIAS.includes(via)) {
		fail(`unknown review route "${via}" (known: ${REVIEW_VIAS.join(", ")})`);
	}
	// an unavailable route is an error, never a silent fall-back to
	// self-review
	if (via === "codex" && !config.capabilities.crossCli) {
		fail(
			"review via codex needs the crossCli capability — enable it in .stdd/config.json (capabilities.crossCli) or use --via subagent",
		);
	}
	if (via === "subagent" && !config.capabilities.subagents) {
		fail(
			"review via subagent needs the subagents capability — enable it in .stdd/config.json (capabilities.subagents) or use --via codex",
		);
	}
	const dispatchBranch = requireBranch(cwd);
	const snapshot = reviewSnapshot(cwd, config.baseRef, true);
	const brief = buildReviewBrief(cwd, config);
	// random, not derived: two requests in the same millisecond over the
	// same snapshot must never share an id
	const id = `rev-${randomBytes(4).toString("hex")}`;
	// the brief can carry source contents: private temp dir (0700), file
	// 0600 — never world-readable under a default umask
	const briefDir = fs.mkdtempSync(path.join(os.tmpdir(), "stdd-review-"));
	const briefPath = path.join(briefDir, `${id}.md`);
	fs.writeFileSync(briefPath, brief, { mode: 0o600 });
	appendLedger(cwd, {
		event: "review-request",
		id,
		via,
		snapshot,
		brief: sha256(brief),
	});
	if (via === "subagent") {
		console.log(`stdd review: brief written to ${briefPath}`);
		console.log("dispatch a fresh READ-ONLY reviewer with that file — never this session's history —");
		console.log("then record its JSON result: stdd review --result <file|->");
		process.exit(0);
	}
	const bin = process.env.STDD_CODEX_BIN || "codex";
	const outPath = path.join(briefDir, "last-message.txt");
	console.log(`stdd review: dispatching ${bin} exec --sandbox read-only (timeout ${timeoutSec}s)…`);
	// the brief travels over stdin (prompt arg "-"): one argv element caps
	// out around 128 KB on Linux, and stdin closes at EOF — codex never
	// hangs on "Reading additional input from stdin..."
	const spawn = spawnSync(
		bin,
		["exec", "--sandbox", "read-only", "--output-last-message", outPath, "-"],
		{
			cwd,
			encoding: "utf8",
			input: brief,
			timeout: timeoutSec * 1000,
			maxBuffer: 64 * 1024 * 1024,
		},
	);
	const runnerFailed = Boolean(spawn.error) || spawn.status !== 0;
	const last = fs.existsSync(outPath) ? fs.readFileSync(outPath, "utf8") : (spawn.stdout ?? "");
	fs.rmSync(briefDir, { recursive: true, force: true });
	// the ledger is branch-scoped: a checkout that switched branches while
	// the reviewer ran would record the verdict on the wrong branch —
	// record nothing; the request stays open on the original branch
	if (requireBranch(cwd) !== dispatchBranch) {
		fail(
			`the checkout switched branches while the reviewer ran — nothing recorded; rerun \`stdd review\` on ${dispatchBranch}`,
		);
	}
	// the runner may take minutes — an approval only counts for the diff
	// the reviewer actually saw, so the snapshot is recomputed on return
	const after = reviewSnapshot(cwd, config.baseRef, true);
	const wentStale = !runnerFailed && after !== snapshot;
	const parsed = runnerFailed || wentStale ? null : parseReviewResult(last);
	recordReview(cwd, {
		id,
		via,
		snapshot: after,
		parsed,
		runner: {
			command: `${bin} exec`,
			exit: spawn.status,
			...(spawn.error
				? { error: spawn.error.code === "ETIMEDOUT" ? "timeout" : String(spawn.error.message) }
				: {}),
		},
		reason: runnerFailed
			? spawn.error?.code === "ETIMEDOUT"
				? `the reviewer timed out after ${timeoutSec}s`
				: `the reviewer process failed (exit ${spawn.status ?? "—"})`
			: wentStale
				? "stale: the checkout changed while the reviewer ran — run `stdd review` again"
				: parsed
					? null
					: "malformed reviewer output — expected the documented JSON object",
	});
}

/**
 * The branch's PR as `stdd status` reports it. Degrades, never errors:
 * no gh on PATH or an unexpected gh failure → state "unknown".
 */
function statusPr(cwd) {
	let raw;
	try {
		raw = execFileSync("gh", ["pr", "view", "--json", "number,url,statusCheckRollup"], {
			cwd,
			encoding: "utf8",
			stdio: ["ignore", "pipe", "pipe"],
		});
	} catch (err) {
		if (err.code === "ENOENT") return { state: "unknown", reason: "gh unavailable" };
		const stderr = err.stderr?.toString() ?? "";
		if (/no pull requests found/i.test(stderr)) return { state: "none" };
		return { state: "unknown", reason: stderr.trim().split("\n")[0] || "gh failed" };
	}
	let info;
	try {
		info = JSON.parse(raw);
	} catch {
		return { state: "unknown", reason: "gh returned unparseable output" };
	}
	const checks = { success: 0, failure: 0, pending: 0 };
	for (const check of info.statusCheckRollup ?? []) {
		const conclusion = (check.conclusion ?? "").toUpperCase();
		if (conclusion === "SUCCESS" || conclusion === "NEUTRAL" || conclusion === "SKIPPED") {
			checks.success++;
		} else if (conclusion === "") {
			checks.pending++;
		} else {
			checks.failure++;
		}
	}
	return { state: "open", number: info.number, url: info.url, checks };
}

/** One rollup entry → { name, terminal, ok }, for check runs and statuses. */
function normalizeCheck(entry) {
	const startedAt = entry.startedAt ?? entry.createdAt ?? "";
	if (entry.state !== undefined && entry.status === undefined) {
		const state = (entry.state ?? "").toUpperCase();
		return {
			name: entry.context ?? "unknown",
			terminal: state !== "" && state !== "EXPECTED" && state !== "PENDING",
			ok: state === "SUCCESS",
			startedAt,
		};
	}
	const conclusion = (entry.conclusion ?? "").toUpperCase();
	return {
		name: entry.name ?? "unknown",
		terminal: (entry.status ?? "").toUpperCase() === "COMPLETED",
		ok: conclusion === "SUCCESS" || conclusion === "NEUTRAL" || conclusion === "SKIPPED",
		startedAt,
	};
}

/** The PR's checks for its current head, straight from the forge. */
function ghPrChecks(pr) {
	const args = ["pr", "view", ...(pr ? [pr] : []), "--json", "number,url,headRefOid,statusCheckRollup"];
	let raw;
	try {
		raw = execFileSync("gh", args, { encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] });
	} catch (err) {
		if (err.code === "ENOENT") fail("stdd ci needs the GitHub CLI (gh) on PATH");
		const stderr = err.stderr?.toString() ?? "";
		if (/no pull requests found/i.test(stderr)) {
			fail("no PR for the current branch — open it first, then watch it settle");
		}
		fail(`gh pr view failed: ${stderr.trim().split("\n")[0] || err.message}`);
	}
	let info;
	try {
		info = JSON.parse(raw);
	} catch {
		fail("gh returned unparseable output");
	}
	return {
		number: info.number,
		url: info.url,
		head: info.headRefOid,
		checks: dedupeChecks((info.statusCheckRollup ?? []).map(normalizeCheck)),
	};
}

/**
 * `stdd ci [pr] [--watch]` — the PR's checks on its **current head**. The
 * watch settles only when the check set is stable (two consecutive polls
 * with the same names) and fully terminal: a watcher attached right after
 * a push sees a partial set — the classic early-settle trap. A terminal
 * failure exits immediately; a head move (amend, force-push) restarts the
 * watch on the new head.
 */
async function ciCommand(pr, watch, intervalSec, timeoutSec) {
	const render = (c) => `  ${c.terminal ? (c.ok ? "✓" : "✗") : "…"} ${c.name}`;
	let localHead = null;
	try {
		localHead = git("rev-parse", "HEAD");
	} catch {
		// outside a git repo gh still resolves an explicit PR number
	}
	let lastHead = null;
	let lastNames = null;
	let lastProgress = null;
	let stable = 0;
	const deadline = Date.now() + timeoutSec * 1000;
	for (;;) {
		const view = ghPrChecks(pr);
		if (view.head !== lastHead) {
			if (lastHead === null) {
				console.log(`PR #${view.number} — checks on head ${view.head.slice(0, 7)} (${view.url})`);
				if (localHead && localHead !== view.head) {
					console.error(
						`stdd ci: local HEAD ${localHead.slice(0, 7)} differs from the PR head — ` +
							"watching the PR head; push if you meant your local commit",
					);
				}
			} else {
				console.log(`head moved to ${view.head.slice(0, 7)} — restarting the watch`);
			}
			lastHead = view.head;
			lastNames = null;
			stable = 0;
		}
		const failed = view.checks.filter((c) => c.terminal && !c.ok);
		const allTerminal = view.checks.every((c) => c.terminal);
		const names = view.checks
			.map((c) => c.name)
			.sort()
			.join("\n");

		if (!watch) {
			for (const c of view.checks) console.log(render(c));
			if (view.checks.length === 0) console.log("  (no checks reported)");
			if (failed.length > 0) {
				console.error(`stdd ci: ${failed.length} failing on ${view.head.slice(0, 7)}`);
				process.exit(1);
			}
			if (allTerminal && view.checks.length > 0) {
				console.log(`stdd ci: green (${view.checks.length} checks) on ${view.head.slice(0, 7)}`);
				return;
			}
			console.error(
				"stdd ci: not settled — checks pending or not yet registered; `stdd ci --watch` waits it out",
			);
			process.exit(1);
		}

		if (failed.length > 0) {
			for (const c of view.checks) console.log(render(c));
			console.error(`stdd ci: ${failed.length} failing check(s) on ${view.head.slice(0, 7)}`);
			process.exit(1);
		}
		stable = allTerminal && view.checks.length > 0 && names === lastNames ? stable + 1 : 0;
		lastNames = names;
		if (stable >= 1 && allTerminal) {
			// second consecutive all-terminal poll with an unchanged set
			for (const c of view.checks) console.log(render(c));
			console.log(
				`stdd ci: green (${view.checks.length} checks) on ${view.head.slice(0, 7)} — terminal`,
			);
			return;
		}
		const progress = `  ${view.checks.filter((c) => c.terminal).length}/${view.checks.length} terminal`;
		if (progress !== lastProgress) {
			console.log(progress);
			lastProgress = progress;
		}
		if (Date.now() > deadline) {
			const open = view.checks.filter((c) => !c.terminal).map((c) => c.name);
			fail(
				`timed out after ${timeoutSec}s — still not terminal: ` +
					(open.join(", ") || "check set still settling"),
			);
		}
		await new Promise((resolve) => setTimeout(resolve, intervalSec * 1000));
	}
}

/**
 * `stdd status` — the next-step oracle. Inputs in order of trust: git (diff
 * against the configured baseRef, dirty state), the ledger, then the forge.
 */
function status(cwd, asJson) {
	const config = loadConfig(cwd);
	const branch = requireBranch(cwd);
	const events = loadLedger(cwd, branch);

	// git: changed files = committed diff vs baseRef + dirty working tree
	let changed = null;
	if (config.baseRef) {
		try {
			const diff = execFileSync("git", ["-C", cwd, "diff", "--name-only", `${config.baseRef}...HEAD`], {
				encoding: "utf8",
				stdio: ["ignore", "pipe", "pipe"],
			});
			const dirty = execFileSync("git", ["-C", cwd, "status", "--porcelain"], {
				encoding: "utf8",
				stdio: ["ignore", "pipe", "pipe"],
			});
			changed = [
				...new Set([
					...diff.split("\n").filter(Boolean),
					...dirty
						.split("\n")
						.filter(Boolean)
						.map((line) => line.slice(3).trim()),
				]),
			];
		} catch {
			changed = null; // unresolvable baseRef — report unknown, never error
		}
	}
	const canonical = config.canonicalDocs.map(globToRegExp);
	const docsChanged = (changed ?? []).filter((f) => canonical.some((re) => re.test(f)));
	const nonDocChanged = (changed ?? []).filter((f) => !canonical.some((re) => re.test(f)));

	const docsEvent = events.filter((e) => e.event === "docs").at(-1) ?? null;
	const lastRedIdx = events.findLastIndex((e) => e.event === "red");
	const redEvent = lastRedIdx === -1 ? null : events[lastRedIdx];
	const verifyEvent =
		events
			.slice(lastRedIdx + 1)
			.filter((e) => e.event === "verify" && e.exit === 0)
			.at(-1) ?? null;

	const loop = {
		docs: docsEvent
			? { done: true, source: "ledger", decision: docsEvent.decision, paths: docsEvent.paths }
			: docsChanged.length > 0
				? { done: true, source: "diff", paths: docsChanged }
				: { done: false },
		red: redEvent
			? { done: true, genuine: redEvent.genuine, cmd: redEvent.cmd, exit: redEvent.exit }
			: { done: false },
		impl: { done: nonDocChanged.length > 0 },
		verify: verifyEvent ? { done: true, cmd: verifyEvent.cmd, exit: verifyEvent.exit } : { done: false },
	};
	const scopeEvent = events.filter((e) => e.event === "scope").at(-1) ?? null;
	const slice = scopeEvent
		? {
				declared: true,
				frozenPaths: scopeEvent.frozenPaths,
				allowedPaths: scopeEvent.allowedPaths,
			}
		: { declared: false };
	// the durable plan: a checkbox is a claim; [red:]-tagged items need the
	// ledger's proof (see planProgress)
	const reviewEvents = events.filter((e) => e.event === "review");
	const latestReview = reviewEvents.at(-1) ?? null;
	// a stale approval reopens the review everywhere: for grading purposes
	// it stops being the newest approval
	const reviewStale =
		latestReview?.verdict === "approved" &&
		latestReview.snapshot !== reviewSnapshot(cwd, config.baseRef);
	const gradableReviews = reviewStale
		? [...reviewEvents, { event: "review", verdict: "stale" }]
		: reviewEvents;
	const planPath = path.join(cwd, PLAN_REL);
	const plan = fs.existsSync(planPath)
		? (() => {
				const parsed = parsePlan(fs.readFileSync(planPath, "utf8"));
				const p = planProgress(
					parsed,
					events.filter((e) => e.event === "red"),
					gradableReviews,
				);
				const pick = (i) => ({ text: i.text, line: i.line, red: i.red, review: i.review });
				// a [review:]-tagged item is the closing review and closes only
				// through the ledger; untagged plans fall back to the LAST item
				// mentioning "review" — a mid-plan "review X" step never counts
				const tagged = parsed.items.find((i) => i.review) ?? null;
				const lastItem = parsed.items.at(-1) ?? null;
				const heuristic = lastItem && /\breview\b/i.test(lastItem.text) ? lastItem : null;
				const reviewItem = tagged ?? heuristic;
				const reviewDone = tagged
					? tagged.checked && latestReview?.verdict === "approved" && !reviewStale
					: (reviewItem?.checked ?? false);
				return {
					present: true,
					total: p.total,
					done: p.done,
					deferred: parsed.deferred.length,
					next: p.next ? pick(p.next) : null,
					unproven: p.unproven.map(pick),
					review: reviewItem ? { present: true, done: reviewDone } : { present: false },
				};
			})()
		: { present: false };
	const pr = statusPr(cwd);
	const trunc = (s) => (s.length > 72 ? `${s.slice(0, 69)}…` : s);

	let next;
	if (pr.state === "open" && pr.checks.failure > 0) {
		// a red required check on an open PR outranks everything — pr-green
		next = `drive PR #${pr.number}'s required checks terminal-green (pr-green playbook)`;
	} else if (!loop.docs.done) {
		next = "make the docs decision: edit the canonical docs, or record `stdd docs <decision>`";
	} else if (!loop.red.done) {
		next = "write the failing test and record it via `stdd red -- <cmd>`";
	} else if (!loop.impl.done) {
		next = "implement until the red test passes";
	} else if (!loop.verify.done) {
		next = "run the narrowest verify lane via `stdd verify -- <cmd>`";
	} else if (plan.present && plan.next) {
		if (plan.unproven.some((u) => u.line === plan.next.line)) {
			next = plan.next.review
				? `plan item "${trunc(plan.next.text)}" is checked but the review is unproven — run \`stdd review\``
				: `plan item "${trunc(plan.next.text)}" is checked but unproven — ` +
					`record \`stdd red -- <cmd containing "${plan.next.red}">\` or uncheck it`;
		} else if (plan.next.review) {
			next = `run \`stdd review\` — the closing review closes "${trunc(plan.next.text)}"`;
		} else {
			next = `continue the plan (${plan.done}/${plan.total} done) — next item: "${trunc(plan.next.text)}"`;
		}
	} else if (pr.state === "none") {
		// the closing review rides on a dispatch capability — with both routes
		// off the suggestion is omitted, never degraded to self-review; a
		// plan whose own review item is checked is not asked twice
		const caps = config.capabilities ?? {};
		const reviewed = plan.present && plan.review?.present && plan.review.done;
		const review =
			!reviewed && (caps.subagents || caps.crossCli)
				? "dispatch a fresh reviewer over the diff (delegate-slice playbook), then "
				: "";
		next = scopeEvent
			? `run \`stdd scope\` (slice postflight), then ${review}draft the evidence line via \`stdd evidence\` and open the PR`
			: `${review}draft the evidence line via \`stdd evidence\`, then open the PR`;
	} else if (pr.state === "open" && (pr.checks.failure > 0 || pr.checks.pending > 0)) {
		next = `drive PR #${pr.number}'s required checks terminal-green (pr-green playbook)`;
	} else if (pr.state === "open") {
		next = `PR #${pr.number} checks are green — done pending review and merge`;
	} else {
		next = `PR state unknown (${pr.reason}) — draft the evidence line via \`stdd evidence\` if no PR exists yet`;
	}

	const reviewState = latestReview
		? {
				verdict: latestReview.verdict,
				via: latestReview.via,
				blocking: (latestReview.findings ?? []).filter((f) => f.severity === "blocking").length,
				stale: reviewStale,
			}
		: null;
	if (asJson) {
		console.log(
			JSON.stringify({ branch, loop, slice, plan, review: reviewState, pr, next }, null, "\t"),
		);
		return;
	}
	const mark = (step) => (step.done ? "✓" : "✗");
	const docsDetail = docsEvent
		? ` (${docsEvent.decision}${docsEvent.paths?.length ? `: ${docsEvent.paths.join(", ")}` : ""})`
		: docsChanged.length > 0
			? ` (diff: ${docsChanged.join(", ")})`
			: changed === null
				? " — unknown (no resolvable baseRef)"
				: " — no docs decision recorded, no canonical docs in the diff";
	const redDetail = redEvent
		? ` (genuine: ${redEvent.genuine}, exit ${redEvent.exit}: ${redEvent.cmd})`
		: " — no red recorded";
	const implDetail = loop.impl.done
		? " (working tree has non-doc changes)"
		: " — no non-doc changes yet";
	const verifyDetail = verifyEvent
		? ` (exit 0: ${verifyEvent.cmd})`
		: " — no passing verify recorded since the last red";
	const prLine =
		pr.state === "open"
			? `#${pr.number} — ${pr.checks.failure} failing, ${pr.checks.pending} pending, ${pr.checks.success} green`
			: pr.state === "none"
				? `none for ${branch}`
				: `unknown (${pr.reason})`;
	console.log(
		[
			`loop:   docs ${mark(loop.docs)}${docsDetail}`,
			`        red  ${mark(loop.red)}${redDetail}`,
			`        impl ${mark(loop.impl)}${implDetail}`,
			`        verify ${mark(loop.verify)}${verifyDetail}`,
			...(scopeEvent
				? [
						`slice:  declared (frozen: ${scopeEvent.frozenPaths.join(", ") || "—"}; ` +
							`allowed: ${scopeEvent.allowedPaths.join(", ") || "—"}) — postflight: stdd scope`,
					]
				: []),
			...(plan.present
				? [
						`plan:   ${plan.total === 0 ? "no checklist items" : `${plan.done}/${plan.total} done`}` +
							(plan.deferred > 0 ? ` (${plan.deferred} deferred)` : "") +
							(plan.next
								? ` — next: "${trunc(plan.next.text)}"`
								: plan.total > 0
									? " — all items closed"
									: ""),
						...plan.unproven.map((u) =>
							u.review
								? `        unproven: "${trunc(u.text)}" — checked, but the newest recorded review is not an approval`
								: `        unproven: "${trunc(u.text)}" — checked, but no recorded red matches "${u.red}"`,
						),
					]
				: []),
			...(reviewState
				? [
						`review: ${
							reviewState.verdict === "approved"
								? `approved via ${reviewState.via}${reviewState.stale ? " — STALE, the checkout changed since" : ""}`
								: reviewState.verdict === "changes-requested"
									? `changes requested via ${reviewState.via} — ${reviewState.blocking} blocking`
									: `error via ${reviewState.via} — rerun \`stdd review\``
						}`,
					]
				: []),
			`pr:     ${prLine}`,
			`next:   ${next}`,
		].join("\n"),
	);
}

/**
 * `stdd status --gate` — the review state as an exit code for hooks.
 * Fails on broken claims (checked-but-unproven review, changes-requested,
 * error, stale approval, impossible route), never on unfinished work.
 */
function statusGate(cwd) {
	const config = loadConfig(cwd);
	const branch = requireBranch(cwd);
	const events = loadLedger(cwd, branch);
	const reasons = [];
	const via = config.review.via;
	if (via === "codex" && !config.capabilities.crossCli) {
		reasons.push('review.via is "codex" but the crossCli capability is off');
	}
	if (via === "subagent" && !config.capabilities.subagents) {
		reasons.push('review.via is "subagent" but the subagents capability is off');
	}
	const latest = events.filter((e) => e.event === "review").at(-1) ?? null;
	if (latest?.verdict === "changes-requested") {
		const blocking = (latest.findings ?? []).filter((f) => f.severity === "blocking").length;
		reasons.push(
			`the newest review requested changes (${blocking} blocking) — fix and rerun \`stdd review\``,
		);
	}
	if (latest?.verdict === "error") {
		reasons.push(`the newest review errored (${latest.reason ?? "unknown"}) — rerun \`stdd review\``);
	}
	if (latest?.verdict === "approved" && latest.snapshot !== reviewSnapshot(cwd, config.baseRef)) {
		reasons.push("the approved review is stale — the checkout changed since; rerun `stdd review`");
	}
	const planPath = path.join(cwd, PLAN_REL);
	if (fs.existsSync(planPath)) {
		const parsed = parsePlan(fs.readFileSync(planPath, "utf8"));
		const claimed = parsed.items.some((i) => i.review && i.checked);
		if (claimed && latest?.verdict !== "approved") {
			reasons.push("a [review:] item is checked but no approved review is recorded — run `stdd review`");
		}
	}
	if (reasons.length === 0) {
		console.log("stdd status --gate: ok");
		process.exit(0);
	}
	for (const r of reasons) console.log(`✗ ${r}`);
	process.exit(1);
}

/**
 * Draft the docs evidence line from the actual diff instead of recall.
 * Canonical docs changed → the finished line on stdout (substitution-safe);
 * none changed → the authored-sentinel templates on stderr, nonzero exit.
 */
function evidence(targetDir, baseRefFlag) {
	const config = loadConfig(targetDir);
	const base = baseRefFlag ?? config.baseRef;
	if (!base) {
		fail('evidence needs a base ref — pass --base <ref> or set "baseRef" in .stdd/config.json');
	}
	let changed;
	try {
		changed = execFileSync("git", ["-C", targetDir, "diff", "--name-only", `${base}...HEAD`], {
			encoding: "utf8",
			stdio: ["ignore", "pipe", "pipe"],
		})
			.split("\n")
			.filter(Boolean);
	} catch (err) {
		fail(`--base ${base}: git diff failed: ${err.stderr?.toString().trim() || err.message}`);
	}
	const canonical = config.canonicalDocs.map(globToRegExp);
	const docs = changed.filter((file) => canonical.some((re) => re.test(file)));
	// The ledger's recorded decision is read first; the diff is the
	// cross-check, and on contradiction the diff wins and the conflict is
	// reported. loadLedger needs a branch — outside a git repo there is no
	// ledger to consult, so degrade to diff-only.
	const evidenceBranch = currentBranch(targetDir);
	const recorded = evidenceBranch
		? (loadLedger(targetDir, evidenceBranch)
				.filter((e) => e.event === "docs")
				.at(-1) ?? null)
		: null;
	if (docs.length > 0) {
		if (recorded && recorded.decision !== "updated-first") {
			console.error(
				`stdd evidence: the ledger records "${recorded.decision}" but the diff changes ` +
					`canonical docs — the diff wins; the ledger claim is contradicted`,
			);
		}
		console.log(`Docs updated first: ${docs.join(", ")}`);
		return;
	}
	if (recorded?.decision === "checked") {
		console.log(`Docs checked, no change needed: ${recorded.paths.join(", ")} — ${recorded.reason}`);
		return;
	}
	if (recorded?.decision === "not-applicable") {
		console.log(`Docs not applicable: ${recorded.reason}`);
		return;
	}
	fail(
		`no canonical docs changed against ${base}${
			recorded ? ` — the ledger claims "updated-first" but the diff is contradicted` : ""
		} — author the evidence line yourself:\n` +
			"  Docs checked, no change needed: <docs + reason>\n" +
			"  Docs not applicable: <why implementation-only>",
	);
}

function checkPr(prBodyFile, baseRef, pr) {
	let body;
	let headRef = "HEAD";
	let okSuffix = "";
	if (pr) {
		if (prBodyFile) fail(`--pr replaces the <file|-> argument — drop "${prBodyFile}"`);
		if (baseRef) fail("--pr derives the base from the PR — drop --base");
		const live = resolveLivePr(pr);
		body = live.body;
		baseRef = live.baseRef;
		headRef = live.headRef;
		const shortHead = (headRef === "HEAD" ? git("rev-parse", "HEAD") : headRef).slice(0, 7);
		okSuffix = ` (PR #${live.number} body, base ${baseRef}, head ${shortHead})`;
	} else {
		body =
			prBodyFile === "-" || !prBodyFile
				? fs.readFileSync(0, "utf8")
				: fs.readFileSync(prBodyFile, "utf8");
	}
	const matches = findEvidenceLines(body);
	if (matches.length === 0) {
		let message =
			"PR body has no docs evidence line. Add exactly one of:\n" +
			"  Docs updated first: <docs>\n" +
			"  Docs checked, no change needed: <docs + reason>\n" +
			"  Docs not applicable: <why implementation-only>";
		for (const near of nearMissEvidenceLines(body)) {
			message +=
				`\n\nline ${near.line} is a near-miss:\n` +
				`  found: ${near.raw.trim()}\n` +
				`  fix:   ${near.suggestion}\n` +
				"  (the line must start at column 0 with the exact label, no markdown formatting)";
		}
		fail(message);
	}
	if (matches.length > 1) {
		fail(
			`PR body has ${matches.length} docs evidence lines ` +
				`(lines ${matches.map((m) => m.line).join(", ")}) — keep exactly one.`,
		);
	}
	if (matches[0].content === "") {
		fail(`"${matches[0].label}:" names no evidence — list the docs or the reason after the colon.`);
	}
	const repoDir = resolveRepoDir(process.cwd());
	if (baseRef) verifyEvidenceAgainstDiff(matches[0], baseRef, headRef, repoDir);
	// Advisory only — a ledger disagreement never changes the pass condition.
	const advisoryBranch = currentBranch(repoDir);
	const recorded = advisoryBranch
		? (loadLedger(repoDir, advisoryBranch)
				.filter((e) => e.event === "docs")
				.at(-1) ?? null)
		: null;
	if (recorded && LABEL_TO_DECISION[matches[0].label] !== recorded.decision) {
		console.error(
			`stdd check-pr: advisory — the ledger records the docs decision as ` +
				`"${recorded.decision}" but the PR body says "${matches[0].label}"`,
		);
	}
	console.log(`stdd check-pr: OK${okSuffix}`);
}

/** With --base: the evidence claim must be backed by the actual git diff. */
function verifyEvidenceAgainstDiff(
	{ label, content },
	baseRef,
	headRef = "HEAD",
	repoDir = process.cwd(),
) {
	const paths = extractDocPaths(content);
	if (label === "Docs updated first") {
		if (paths.length === 0) {
			const suggestion = sentinelSuggestion(content);
			fail(
				`"Docs updated first:" names no doc paths — list the changed docs.` +
					(suggestion ? `\nDid you mean:\n  ${suggestion}` : ""),
			);
		}
		let changed;
		try {
			changed = execFileSync("git", ["diff", "--name-only", `${baseRef}...${headRef}`], {
				encoding: "utf8",
				stdio: ["ignore", "pipe", "pipe"],
			})
				.split("\n")
				.filter(Boolean);
		} catch (err) {
			fail(`--base ${baseRef}: git diff failed: ${err.stderr?.toString().trim() || err.message}`);
		}
		const missing = paths.filter((p) => !changed.includes(p));
		if (missing.length > 0) {
			fail(`claimed as updated but not changed against ${baseRef}: ${missing.join(", ")}`);
		}
	} else if (label === "Docs checked, no change needed") {
		const absent = paths.filter((p) => !fs.existsSync(path.join(repoDir, ...p.split("/"))));
		if (absent.length > 0) {
			fail(`claimed as checked but does not exist in the tree: ${absent.join(", ")}`);
		}
	}
}

