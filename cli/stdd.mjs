const STAMP = `generated by stdd v${VERSION} — do not edit, re-run \`stdd init\``;

function fail(message) {
	console.error(`stdd: ${message}`);
	process.exit(1);
}

function listTrackedFiles(dir) {
	try {
		const out = execFileSync("git", ["-C", dir, "ls-files", "-z"], {
			encoding: "utf8",
			stdio: ["ignore", "pipe", "ignore"],
		});
		const files = out.split("\0").filter(Boolean);
		if (files.length > 0) return files;
	} catch {
		// not a git repo — fall through to a filesystem walk
	}
	const files = [];
	const walk = (rel) => {
		for (const entry of fs.readdirSync(path.join(dir, rel), { withFileTypes: true })) {
			if (entry.name === ".git" || entry.name === "node_modules") continue;
			const relPath = rel ? `${rel}/${entry.name}` : entry.name;
			if (entry.isDirectory()) walk(relPath);
			else files.push(relPath);
		}
	};
	walk("");
	return files;
}

function loadPlaybooks() {
	const dir = path.join(PKG_ROOT, "playbooks");
	return fs
		.readdirSync(dir)
		.filter((f) => f.endsWith(".md"))
		.map((f) => {
			const source = fs.readFileSync(path.join(dir, f), "utf8");
			const { meta, body } = parseFrontmatter(source);
			if (!meta.name || !meta.description) {
				fail(`playbook ${f} is missing required frontmatter (name, description)`);
			}
			return { file: f, source, meta, body };
		});
}

/**
 * Project recipes: repo-owned playbooks in `.stdd/playbooks/local/`, same
 * frontmatter contract as the kit's. Never generated, never overwritten.
 */
function loadLocalPlaybooks(targetDir) {
	const dir = path.join(targetDir, ".stdd", "playbooks", "local");
	if (!fs.existsSync(dir)) return [];
	return fs
		.readdirSync(dir)
		.filter((f) => f.endsWith(".md"))
		.map((f) => {
			const source = fs.readFileSync(path.join(dir, f), "utf8");
			const { meta, body } = parseFrontmatter(source);
			if (!meta.name || !meta.description) {
				fail(`local playbook local/${f} is missing required frontmatter (name, description)`);
			}
			return { file: f, source, meta, body };
		});
}

/** Files recorded by the previous init, or {} without a readable manifest. */
function readManifestFiles(stddDir) {
	try {
		return JSON.parse(fs.readFileSync(path.join(stddDir, "manifest.json"), "utf8")).files ?? {};
	} catch {
		return {};
	}
}

/**
 * `--capabilities <list>`: write the full profile into the user-owned
 * config — named capabilities on, every other known one off. Other config
 * keys survive untouched.
 */
function writeCapabilities(targetDir, list) {
	const configPath = path.join(targetDir, ".stdd", "config.json");
	let parsed = { ...DEFAULT_CONFIG };
	if (fs.existsSync(configPath)) {
		try {
			parsed = JSON.parse(fs.readFileSync(configPath, "utf8"));
		} catch (err) {
			fail(`.stdd/config.json is not valid JSON: ${err.message}`);
		}
	}
	parsed.capabilities = Object.fromEntries(KNOWN_CAPABILITIES.map((c) => [c, list.includes(c)]));
	fs.writeFileSync(configPath, `${JSON.stringify(parsed, null, "\t")}\n`);
}

/**
 * `--session-hook`: merge a SessionStart hook running `stdd status` into
 * `.claude/settings.json` — every fresh context window (startup, clear,
 * compact) opens with the loop state in it. Merged, never duplicated; a
 * settings file that does not parse is left untouched.
 */
function installSessionHook(targetDir) {
	const settingsPath = path.join(targetDir, ".claude", "settings.json");
	const entry = {
		matcher: "startup|clear|compact",
		hooks: [{ type: "command", command: "npx --no stdd status || true" }],
	};
	let settings = {};
	if (fs.existsSync(settingsPath)) {
		try {
			settings = JSON.parse(fs.readFileSync(settingsPath, "utf8"));
		} catch {
			console.error(
				".claude/settings.json does not parse — left untouched; merge the hook manually:\n  " +
					JSON.stringify({ hooks: { SessionStart: [entry] } }),
			);
			return;
		}
	}
	settings.hooks ??= {};
	settings.hooks.SessionStart ??= [];
	if (JSON.stringify(settings.hooks.SessionStart).includes("stdd status")) {
		console.log(".claude/settings.json already carries the session-start hook — left untouched");
		return;
	}
	settings.hooks.SessionStart.push(entry);
	fs.mkdirSync(path.dirname(settingsPath), { recursive: true });
	fs.writeFileSync(settingsPath, `${JSON.stringify(settings, null, "\t")}\n`);
	console.log(
		"Wired the Claude Code SessionStart hook (startup|clear|compact → stdd status) in .claude/settings.json",
	);
}

/**
 * `--interview`: one question at a time, the recommended answer first —
 * an empty answer takes it. Piped answers work; a stream that ends early
 * resolves every remaining question to its default.
 */
async function interview() {
	const rl = createInterface({ input: process.stdin, output: process.stdout });
	// Own line queue instead of rl.question: piped answers arrive in one
	// burst, and lines emitted while no question is pending would be lost.
	const lines = [];
	const waiters = [];
	let closed = false;
	rl.on("line", (line) => {
		const waiter = waiters.shift();
		if (waiter) waiter(line);
		else lines.push(line);
	});
	rl.on("close", () => {
		closed = true;
		while (waiters.length > 0) waiters.shift()("");
	});
	const readLine = () => {
		if (lines.length > 0) return Promise.resolve(lines.shift());
		if (closed) return Promise.resolve("");
		return new Promise((resolve) => waiters.push(resolve));
	};
	const ask = async (question, def) => {
		process.stdout.write(question);
		const answer = (await readLine()).trim();
		return answer === "" ? def : answer;
	};
	const yes = async (question, def) =>
		/^y/i.test(await ask(`${question} ${def ? "[Y/n]" : "[y/N]"} `, def ? "y" : "n"));

	console.log("stdd init — one question at a time; an empty answer takes the recommended default\n");
	const toolsAnswer = await ask(
		`Agents to compile for (${KNOWN_TOOLS.join(", ")}) [${KNOWN_TOOLS.join(",")}]: `,
		KNOWN_TOOLS.join(","),
	);
	const tools = toolsAnswer
		.split(",")
		.map((t) => t.trim())
		.filter(Boolean);
	const unknownTools = tools.filter((t) => !KNOWN_TOOLS.includes(t));
	if (unknownTools.length > 0) {
		rl.close();
		fail(`unknown tool(s): ${unknownTools.join(", ")} (known: ${KNOWN_TOOLS.join(", ")})`);
	}
	const capabilitiesList = [];
	if (await yes("Can agents dispatch subagents?", true)) capabilitiesList.push("subagents");
	if (await yes("May Claude Code and Codex invoke each other?", false))
		capabilitiesList.push("crossCli");
	if (await yes("Are isolated git worktrees available?", true)) capabilitiesList.push("worktrees");
	const ci = (await yes("Install the GitHub Actions gate (stdd check + PR evidence)?", true))
		? ["github"]
		: [];
	const hooks = await yes("Install the pre-push hook (stdd check — fast, offline)?", true);
	const sessionHook = tools.includes("claude")
		? await yes("Wire the Claude Code session-start hook (stdd status)?", true)
		: false;
	rl.close();
	return { tools, ci, hooks, sessionHook, capabilitiesList };
}

function loadConfig(targetDir) {
	const configPath = path.join(targetDir, ".stdd", "config.json");
	if (!fs.existsSync(configPath)) return DEFAULT_CONFIG;
	let parsed;
	try {
		parsed = JSON.parse(fs.readFileSync(configPath, "utf8"));
	} catch (err) {
		fail(`.stdd/config.json is not valid JSON: ${err.message}`);
	}
	try {
		return mergeConfig(parsed);
	} catch (err) {
		fail(`.stdd/config.json: ${err.message}`);
	}
}

function init(targetDir, opts) {
	const { tools, ci, hooks, sessionHook, capabilitiesList } = opts;
	const stddDir = path.join(targetDir, ".stdd");
	fs.mkdirSync(path.join(stddDir, "playbooks"), { recursive: true });
	if (capabilitiesList) writeCapabilities(targetDir, capabilitiesList);
	const capabilities = loadConfig(targetDir).capabilities;
	// The previous run's manifest: files it generated that this profile no
	// longer produces are removed at the end — only when still byte-identical.
	const oldFiles = readManifestFiles(stddDir);
	// Every generated file is recorded here (repo-relative path → content
	// hash) and written to .stdd/manifest.json, so check/doctor can detect
	// hand edits, deletions, and stale copies. User-owned files (config.json,
	// local recipes) are deliberately not recorded.
	const generated = {};
	const writeGenerated = (relPath, content) => {
		fs.writeFileSync(path.join(targetDir, ...relPath.split("/")), content);
		generated[relPath] = sha256(content);
	};

	writeGenerated(".stdd/method.md", fs.readFileSync(path.join(PKG_ROOT, "method", "README.md"), "utf8"));
	// Kit playbooks minus those a local recipe overrides by name, then both
	// sets filtered by `requires:` and compiled against the profile.
	const local = loadLocalPlaybooks(targetDir);
	const localNames = new Set(local.map((pb) => pb.meta.name));
	const kit = loadPlaybooks().filter((pb) => {
		if (!localNames.has(pb.meta.name)) return true;
		console.log(`local recipe overrides the kit playbook "${pb.meta.name}" (${pb.file})`);
		return false;
	});
	const applies = (pb) => {
		if (!pb.meta.requires) return true;
		if (!KNOWN_CAPABILITIES.includes(pb.meta.requires)) {
			fail(`playbook ${pb.file}: requires unknown capability "${pb.meta.requires}"`);
		}
		return capabilities[pb.meta.requires];
	};
	const compile = (pb, text) => {
		try {
			return compileCapabilities(text, capabilities);
		} catch (err) {
			fail(`playbook ${pb.file}: ${err.message}`);
		}
	};
	const kitActive = kit.filter(applies);
	const localActive = local.filter(applies);
	for (const pb of kitActive) {
		writeGenerated(`.stdd/playbooks/${pb.file}`, compile(pb, pb.source));
	}
	const configPath = path.join(stddDir, "config.json");
	if (!fs.existsSync(configPath)) {
		fs.writeFileSync(configPath, `${JSON.stringify(DEFAULT_CONFIG, null, "\t")}\n`);
	}
	console.log(`Installed .stdd/ (method, ${kitActive.length} playbooks, config)`);

	if (tools.includes("claude")) {
		for (const pb of [...kitActive, ...localActive]) {
			const skillDir = path.join(targetDir, ".claude", "skills", pb.meta.name);
			fs.mkdirSync(skillDir, { recursive: true });
			// the description is the only always-visible routing surface — the
			// `when:` trigger must ride on it, not sit unread in the body
			const description = pb.meta.when
				? `${pb.meta.description}. Use when: ${pb.meta.when}`
				: pb.meta.description;
			const skill =
				`---\nname: ${pb.meta.name}\ndescription: ${JSON.stringify(description)}\n---\n\n` +
				`<!-- ${STAMP} -->\n\n${compile(pb, pb.body)}`;
			writeGenerated(`.claude/skills/${pb.meta.name}/SKILL.md`, skill);
		}
		console.log(
			`Installed ${kitActive.length + localActive.length} Claude Code skills under .claude/skills/`,
		);
	}

	if (tools.includes("codex")) {
		const lines = [
			`<!-- ${STAMP} -->`,
			"",
			"## STDD",
			"",
			"This repository follows the STDD method: read `.stdd/method.md` before",
			"any behavior change and follow its loop (docs edit is the spec → failing",
			"test → implement → verify → PR evidence).",
			"",
			"Do not search the project log (dated entries marked",
			"`authority: non-canonical`, e.g. `docs/project/`) unless the user",
			"explicitly asks for historical rationale or deferred work — it records",
			"past decisions and future intentions, never the present.",
			"",
			"If `stdd` is not on PATH, run it via the package runner instead",
			"(`pnpm exec stdd`, `npx --no stdd`) — never skip recording a loop",
			"fact because the bare command failed.",
			...(capabilities.crossCli
				? [
						"",
						"Claude Code and Codex may invoke each other: dispatch the other CLI",
						"headless with a brief file (`codex exec`, `claude -p`) per the",
						"delegate-slice playbook.",
					]
				: []),
			"",
			"Playbooks in `.stdd/playbooks/`:",
			"",
			...kitActive.map((pb) => `- \`${pb.file}\` — ${pb.meta.when ?? pb.meta.description}`),
			...(localActive.length > 0
				? [
						"",
						"Project playbooks in `.stdd/playbooks/local/`:",
						"",
						...localActive.map(
							(pb) => `- \`local/${pb.file}\` — ${pb.meta.when ?? pb.meta.description}`,
						),
					]
				: []),
		];
		const snippet = `${lines.join("\n")}\n`;
		writeGenerated(".stdd/AGENTS-snippet.md", snippet);
		// AGENTS.md is user-owned (never manifest-tracked): only the marked
		// section is managed — created when absent, replaced in place when
		// present, everything outside the markers untouched
		const agentsPath = path.join(targetDir, "AGENTS.md");
		const block = `<!-- stdd:begin — managed section, re-run \`stdd init\` to update -->\n${snippet}<!-- stdd:end -->\n`;
		const markers = /<!-- stdd:begin[^>]*-->\r?\n[\s\S]*?<!-- stdd:end -->\r?\n?/;
		if (!fs.existsSync(agentsPath)) {
			fs.writeFileSync(agentsPath, block);
			console.log("Wrote AGENTS.md with the managed STDD section");
		} else {
			const current = fs.readFileSync(agentsPath, "utf8");
			const updated = markers.test(current)
				? current.replace(markers, block)
				: `${current}${current.endsWith("\n") ? "" : "\n"}\n${block}`;
			if (updated !== current) fs.writeFileSync(agentsPath, updated);
			console.log("Updated the managed STDD section in AGENTS.md");
		}
	}

	if (ci.includes("github")) {
		fs.mkdirSync(path.join(targetDir, ".github", "workflows"), { recursive: true });
		const workflow = fs
			.readFileSync(path.join(PKG_ROOT, "templates", "github-stdd.yml"), "utf8")
			.replace("__STAMP__", STAMP);
		writeGenerated(".github/workflows/stdd.yml", workflow);
		console.log("Installed .github/workflows/stdd.yml (validates the live PR body)");
	}

	if (hooks) {
		// One fast offline command only — network calls in hooks produce
		// false positives that train --no-verify. User-owned after
		// generation (like config.json): never manifested, never overwritten.
		const hookPath = path.join(stddDir, "hooks", "pre-push");
		if (fs.existsSync(hookPath)) {
			console.log(".stdd/hooks/pre-push already exists — left untouched (user-owned)");
		} else {
			fs.mkdirSync(path.join(stddDir, "hooks"), { recursive: true });
			fs.writeFileSync(
				hookPath,
				"#!/bin/sh\n# user-owned after generation — append your own steps freely\n# --no: never install from the network — a hook must be fast and offline\nnpx --no stdd check . || exit 1\n",
				{ mode: 0o755 },
			);
			console.log(
				"Installed .stdd/hooks/pre-push (runs stdd check — fast, offline). Wire it up with ONE of:\n" +
					"  git config core.hooksPath .stdd/hooks   # note: this disables hooks in .git/hooks\n" +
					"  …or call `stdd check` from your existing husky/lefthook pre-push",
			);
		}
	}

	if (sessionHook) installSessionHook(targetDir);

	// The ledger and the plan are per-checkout working artifacts — never
	// committed. The ignore rules are user-owned once written, not manifested.
	const gitignorePath = path.join(targetDir, ".gitignore");
	const gitignore = fs.existsSync(gitignorePath) ? fs.readFileSync(gitignorePath, "utf8") : "";
	const lines = gitignore.split("\n");
	const missing = [".stdd/ledger.jsonl", ".stdd/plan.md"].filter((l) => !lines.includes(l));
	if (missing.length > 0) {
		const sep = gitignore === "" || gitignore.endsWith("\n") ? "" : "\n";
		fs.writeFileSync(gitignorePath, `${gitignore}${sep}${missing.join("\n")}\n`);
		console.log(`Added ${missing.join(", ")} to .gitignore (per-checkout, never committed)`);
	}

	const manifest = { generatedBy: "stdd", version: VERSION, files: generated };
	fs.writeFileSync(path.join(stddDir, "manifest.json"), `${JSON.stringify(manifest, null, "\t")}\n`);

	// A profile or tool change may stop generating a file a previous init
	// wrote. Remove it only when still byte-identical to what init wrote —
	// a hand-edited file is never deleted.
	for (const [file, hash] of Object.entries(oldFiles)) {
		if (file in generated) continue;
		const filePath = path.join(targetDir, ...file.split("/"));
		if (fs.existsSync(filePath) && sha256(fs.readFileSync(filePath, "utf8")) === hash) {
			fs.rmSync(filePath);
			console.log(`Removed ${file} (no longer generated by this profile)`);
		}
	}
}

/**
 * Shared repository scan for `check` and `doctor`: committed working
 * artifacts, canonical docs and their temporal-narrative hits, and stale
 * generated files (a stale stamp means the CLI was upgraded without
 * re-running `stdd init`).
 */
function scanRepo(targetDir, config) {
	const files = listTrackedFiles(targetDir);

	const forbidden = config.forbiddenArtifacts.map(globToRegExp);
	const artifacts = files.filter((file) => forbidden.some((re) => re.test(file)));

	const canonical = config.canonicalDocs.map(globToRegExp);
	const canonicalFiles = files.filter((file) => canonical.some((re) => re.test(file)));
	const matchers = temporalMatchers(config.temporalPhrases);
	const temporal = [];
	for (const file of canonicalFiles) {
		const lines = fs.readFileSync(path.join(targetDir, file), "utf8").split("\n");
		for (const hit of scanTemporal(lines, matchers)) temporal.push({ file, ...hit });
	}

	const contentHits = [];
	let added = null;
	if (config.contentRules.some((r) => r.newFilesOnly)) {
		added = addedFiles(targetDir, config.baseRef);
	}
	for (const rule of config.contentRules) {
		const fileRe = globToRegExp(rule.files);
		let targets = files.filter((f) => fileRe.test(f));
		if (rule.newFilesOnly && added !== null) {
			targets = targets.filter((f) => added.includes(f));
		}
		for (const file of targets) {
			const content = fs.readFileSync(path.join(targetDir, file), "utf8");
			if (rule.forbid) {
				const re = new RegExp(rule.forbid);
				content.split("\n").forEach((line, i) => {
					if (re.test(line)) contentHits.push({ file, line: i + 1, rule });
				});
			}
			if (rule.require && !new RegExp(rule.require, "m").test(content)) {
				contentHits.push({ file, line: null, rule });
			}
		}
	}

	return {
		artifacts,
		bookkeeping: trackedBookkeeping(targetDir),
		canonicalFiles,
		temporal,
		contentHits,
		stale: scanGeneratedDrift(targetDir),
	};
}

/**
 * stdd's own per-checkout bookkeeping (the ledger, the plan) that git
 * tracks anyway. Hardcoded, not config-driven — these files are never
 * legitimate in history. Outside a git repo tracking is unknowable: [].
 */
function trackedBookkeeping(targetDir) {
	try {
		return execFileSync(
			"git",
			["-C", targetDir, "ls-files", "--", ".stdd/ledger.jsonl", ".stdd/plan.md"],
			{ encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] },
		)
			.split("\n")
			.filter(Boolean);
	} catch {
		return [];
	}
}

/** Files added against baseRef, or null when the base is unresolvable. */
function addedFiles(targetDir, baseRef) {
	if (!baseRef) return null;
	try {
		return execFileSync(
			"git",
			["-C", targetDir, "diff", "--diff-filter=A", "--name-only", `${baseRef}...HEAD`],
			{ encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] },
		)
			.split("\n")
			.filter(Boolean);
	} catch {
		return null;
	}
}

/**
 * Generated-file drift, as `{ file, message }` findings. With a manifest
 * (written by `stdd init`), every generated file is verified by content
 * hash. Without one (repo initialized by an older stdd), fall back to the
 * version stamp inside Claude skills.
 */
function scanGeneratedDrift(targetDir) {
	const stale = [];
	const manifestPath = path.join(targetDir, ".stdd", "manifest.json");
	if (fs.existsSync(manifestPath)) {
		let manifest;
		try {
			manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
		} catch {
			return [{ file: ".stdd/manifest.json", message: "is not valid JSON — re-run stdd init" }];
		}
		if (manifest.version !== VERSION) {
			stale.push({
				file: ".stdd/manifest.json",
				message: `written by stdd v${manifest.version}, CLI is v${VERSION} — re-run stdd init`,
			});
		}
		for (const [file, hash] of Object.entries(manifest.files ?? {})) {
			const filePath = path.join(targetDir, ...file.split("/"));
			if (!fs.existsSync(filePath)) {
				stale.push({ file, message: "listed in .stdd/manifest.json but missing — re-run stdd init" });
			} else if (sha256(fs.readFileSync(filePath, "utf8")) !== hash) {
				stale.push({ file, message: "edited by hand or stale — re-run stdd init" });
			}
		}
		return stale;
	}

	const stampRe = /generated by stdd v([0-9][^\s]*) —/;
	const skillsDir = path.join(targetDir, ".claude", "skills");
	if (fs.existsSync(skillsDir)) {
		for (const name of fs.readdirSync(skillsDir)) {
			const skillPath = path.join(skillsDir, name, "SKILL.md");
			if (!fs.existsSync(skillPath)) continue;
			const m = stampRe.exec(fs.readFileSync(skillPath, "utf8"));
			if (m && m[1] !== VERSION) {
				stale.push({
					file: `.claude/skills/${name}/SKILL.md`,
					message: `generated by stdd v${m[1]}, CLI is v${VERSION} — re-run stdd init`,
				});
			}
		}
	}
	return stale;
}

function check(targetDir) {
	const config = loadConfig(targetDir);
	const { artifacts, bookkeeping, temporal, contentHits, stale } = scanRepo(targetDir, config);
	const violations = [
		...artifacts.map((file) => `${file}: committed working artifact (forbiddenArtifacts)`),
		...bookkeeping.map(
			(file) => `${file}: committed stdd working artifact — per-checkout only; add it to .gitignore`,
		),
		...temporal.map(
			(hit) => `${hit.file}:${hit.line}: temporal narrative in canonical doc ("${hit.phrase}")`,
		),
		...contentHits.map(
			(h) =>
				`${h.file}${h.line ? `:${h.line}` : ""}: ${h.rule.name}` +
				(h.rule.message ? ` — ${h.rule.message}` : h.line ? "" : " — required pattern not found"),
		),
		...stale.map((s) => `${s.file}: ${s.message}`),
	];
	// Enforced locally (the pre-push hook runs stdd check); a detached
	// checkout — CI — has no branch name to validate and skips the rule.
	if (config.branchPattern) {
		const branch = currentBranch(targetDir);
		if (branch && branch !== "HEAD" && !new RegExp(config.branchPattern).test(branch)) {
			violations.push(`branch "${branch}" does not match branchPattern ${config.branchPattern}`);
		}
	}

	if (violations.length > 0) {
		console.error(`stdd check: ${violations.length} violation(s)\n`);
		for (const v of violations) console.error(`  ${v}`);
		process.exit(1);
	}
	console.log("stdd check: OK");
}

/**
 * Config-declared worktree readiness: report each missing required path
 * with its repo-authored fix hint. Purely declarative — existence only,
 * nothing is executed or installed. Returns true when everything passes.
 */
function reportReadiness(targetDir, config, report) {
	const required = config.readiness.required;
	if (required.length === 0) return null;
	const missing = required.filter(
		(entry) => !fs.existsSync(path.join(targetDir, ...entry.path.split("/"))),
	);
	for (const entry of missing) {
		report(false, `${entry.path} missing${entry.hint ? ` — ${entry.hint}` : ""}`);
	}
	if (missing.length === 0) {
		report(true, `worktree ready (${required.length}/${required.length} present)`);
	}
	return missing.length === 0;
}

function doctor(targetDir, readinessOnly = false) {
	const count = (n, singular, plural) => `${n} ${n === 1 ? singular : plural}`;
	let failed = false;
	const report = (ok, message) => {
		console.log(`${ok ? "✓" : "✗"} ${message}`);
		if (!ok) failed = true;
	};

	if (readinessOnly) {
		if (reportReadiness(targetDir, loadConfig(targetDir), report) === null) {
			console.log("no readiness contract declared (readiness.required in .stdd/config.json)");
		}
		if (failed) process.exit(1);
		return;
	}

	const installed = fs.existsSync(path.join(targetDir, ".stdd", "method.md"));
	report(installed, installed ? ".stdd/ installed" : ".stdd/ is not installed — run stdd init");

	const config = loadConfig(targetDir);
	reportReadiness(targetDir, config, report);
	const { artifacts, bookkeeping, canonicalFiles, temporal, contentHits, stale } = scanRepo(
		targetDir,
		config,
	);

	if (config.contentRules.length > 0) {
		report(
			contentHits.length === 0,
			contentHits.length === 0
				? `content rules clean (${count(config.contentRules.length, "rule", "rules")})`
				: `${count(contentHits.length, "content-rule violation", "content-rule violations")} — see stdd check`,
		);
	}

	report(
		canonicalFiles.length > 0,
		canonicalFiles.length > 0
			? `canonical docs present (${count(canonicalFiles.length, "file", "files")})`
			: "no canonical docs found — create docs/domain/ or docs/product/, or adjust canonicalDocs",
	);

	const committed = artifacts.length + bookkeeping.length;
	report(
		committed === 0,
		committed === 0
			? "no committed working artifacts"
			: `${count(committed, "committed working artifact", "committed working artifacts")} may mislead coding agents`,
	);

	const temporalFiles = new Set(temporal.map((hit) => hit.file)).size;
	report(
		temporalFiles === 0,
		temporalFiles === 0
			? "canonical docs describe the present (no temporal narrative)"
			: `${count(temporalFiles, "canonical doc contains", "canonical docs contain")} temporal narrative`,
	);

	report(
		stale.length === 0,
		stale.length === 0
			? `generated files match stdd v${VERSION}`
			: `${count(stale.length, "generated file is", "generated files are")} stale — re-run stdd init`,
	);

	// informational only — a missing hook is a choice, never a failure
	const hookInstalled = fs.existsSync(path.join(targetDir, ".stdd", "hooks", "pre-push"));
	console.log(
		hookInstalled
			? "· pre-push hook installed (.stdd/hooks/pre-push) — wire via git config core.hooksPath .stdd/hooks"
			: "· no pre-push hook installed — optional: stdd init --hooks",
	);

	const agentsPath = path.join(targetDir, "AGENTS.md");
	if (fs.existsSync(agentsPath)) {
		const hasSection = /^## STDD$/m.test(fs.readFileSync(agentsPath, "utf8"));
		report(
			hasSection,
			hasSection
				? "AGENTS.md carries the STDD section"
				: "AGENTS.md has no STDD section — run `stdd init --tools codex` to write it",
		);
	}

	const workflowsDir = path.join(targetDir, ".github", "workflows");
	if (fs.existsSync(workflowsDir)) {
		for (const file of fs.readdirSync(workflowsDir).filter((f) => /\.ya?ml$/.test(f))) {
			if (workflowValidatesStaleBody(fs.readFileSync(path.join(workflowsDir, file), "utf8"))) {
				report(
					false,
					`.github/workflows/${file} validates the frozen event payload body — ` +
						"body edits will not be re-checked; see stdd init --ci github",
				);
			}
		}
	}

	for (const rel of [
		"pull_request_template.md",
		".github/pull_request_template.md",
		"docs/pull_request_template.md",
	]) {
		const tplPath = path.join(targetDir, ...rel.split("/"));
		if (!fs.existsSync(tplPath)) continue;
		if (findEvidenceLines(fs.readFileSync(tplPath, "utf8")).length > 0) {
			report(
				false,
				`${rel} carries an unquoted evidence label — placeholder residue would pass ` +
					'the gate on every PR; quote template instructions ("> Docs …")',
			);
		}
	}

	if (failed) process.exit(1);
}

/** Run git in the working directory, returning trimmed stdout; throws on failure. */
function git(...args) {
	return execFileSync("git", args, { encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] }).trim();
}

/**
 * With --pr: fetch the live PR (body, base, head) from the forge, so the
 * validation matches what CI will see — never a diverged local checkout.
 * Returns `{ body, baseRef, headRef, number }`.
 */
function resolveLivePr(pr) {
	const args = [
		"pr",
		"view",
		...(pr === "." ? [] : [pr]),
		"--json",
		"body,baseRefName,headRefOid,number",
	];
	let info;
	try {
		info = JSON.parse(execFileSync("gh", args, { encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] }));
	} catch (err) {
		if (err.code === "ENOENT") fail("--pr needs the GitHub CLI (gh) on PATH");
		fail(`gh pr view failed: ${err.stderr?.toString().trim().split("\n")[0] || err.message}`);
	}
	const baseRef = `origin/${info.baseRefName}`;
	try {
		git("fetch", "-q", "origin", info.baseRefName);
	} catch (err) {
		fail(
			`could not fetch origin/${info.baseRefName}: ${err.stderr?.toString().trim().split("\n")[0] || err.message}`,
		);
	}
	let headRef = "HEAD";
	if (git("rev-parse", "HEAD") !== info.headRefOid) {
		try {
			git("cat-file", "-e", `${info.headRefOid}^{commit}`);
		} catch {
			try {
				git("fetch", "-q", "origin", `pull/${info.number}/head`);
				git("cat-file", "-e", `${info.headRefOid}^{commit}`);
			} catch {
				fail(
					`local HEAD differs from PR head ${info.headRefOid.slice(0, 7)} and it could not ` +
						"be fetched — push or check out the PR branch",
				);
			}
		}
		headRef = info.headRefOid;
	}
	return { body: info.body, baseRef, headRef, number: info.number };
}

